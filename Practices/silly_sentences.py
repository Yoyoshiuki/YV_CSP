num = input("Now give me a good number: ").strip().lower()
ani = input("Perfect! Now give me an animal: ").strip().lower()
verb = input("Now for the verb: ").strip().lower()
prl1= input("Give me a plural noun(s): ").strip().lower()
prl2 = input("Give me another plural noun: ").strip().lower()
prl3 = input("Now finally give me the last plural noun: ").strip().lower()

